const parseCSVDatabase = (csvEvaluadores, csvAlumnos) => {
  const parseCSVLine = (text) => {
    let ret = [], inQuote = false, value = '';
    for (let i = 0; i < text.length; i++) {
      let char = text[i];
      if (inQuote) {
        if (char === '"' && text[i+1] === '"') { value += '"'; i++; }
        else if (char === '"') { inQuote = false; }
        else { value += char; }
      } else {
        if (char === '"') { inQuote = true; }
        else if (char === ',') { ret.push(value.trim()); value = ''; }
        else { value += char; }
      }
    }
    ret.push(value.trim());
    return ret;
  };

  const evaluatorsMap = new Map();
  const dbEvaluadores = [];
  const dbParticipants = [];

  const adminUser = { rfc: 'GOKU', nombre: 'Administrador General', rol: 'Supervisión Estatal', assignedIds: [] };
  dbEvaluadores.push(adminUser);
  evaluatorsMap.set('GOKU', adminUser);

  const evalLines = csvEvaluadores.replace(/\r/g, '').trim().split('\n');
  for (let i = 1; i < evalLines.length; i++) {
    if (!evalLines[i].trim()) continue;
    const cols = parseCSVLine(evalLines[i]);
    const evalName = cols[0]?.trim();
    const evalUser = cols[1]?.trim();
    
    if (!evalName || !evalUser) continue;
    const normalizedName = evalName.toUpperCase();

    const newEval = { rfc: evalUser, nombre: evalName, rol: 'Evaluador Titular', assignedIds: [] };
    dbEvaluadores.push(newEval);
    evaluatorsMap.set(normalizedName, newEval);
  }

  const studentLines = csvAlumnos.replace(/\r/g, '').trim().split('\n');
  if (studentLines.length > 0) {
    const headers = parseCSVLine(studentLines[0]).map(h => h.toUpperCase());
    
    const iNombre = headers.indexOf('NOMBRE COMPLETO DEL NIÑO (A)');
    const iRegion = headers.indexOf('REGION');
    const iSector = headers.indexOf('SECTOR');
    const iSexo = headers.indexOf('SEXO');
    const iCct = headers.indexOf('CCT');
    const iEscuela = headers.indexOf('NOMBRE_ESCUELA');
    const iTipo = headers.indexOf('TIPO');
    const iProyecto = headers.indexOf('NOM_PROYECTO');
    const iEval1 = headers.indexOf('EVALUADOR 1');
    const iEval2 = headers.indexOf('EVALUADOR 2');
    const iEval3 = headers.indexOf('EVALUADOR 3');

    let pIdCounter = 1;

    for (let i = 1; i < studentLines.length; i++) {
      if (!studentLines[i].trim()) continue;
      const cols = parseCSVLine(studentLines[i]);
      
      const studentName = iNombre >= 0 ? cols[iNombre] : `Alumno Sin Nombre ${i}`;
      if (!studentName || studentName === '') continue;

      const newStudent = {
        id: pIdCounter++,
        nombre: studentName,
        escuela: iEscuela >= 0 && cols[iEscuela] ? cols[iEscuela] : '-',
        cct: iCct >= 0 && cols[iCct] ? cols[iCct] : '-',
        region: iRegion >= 0 && cols[iRegion] ? cols[iRegion] : '-',
        sector: iSector >= 0 && cols[iSector] ? cols[iSector] : '-',
        servicio: iTipo >= 0 && cols[iTipo] ? cols[iTipo] : 'No especificado',
        genero: iSexo >= 0 && cols[iSexo] ? cols[iSexo] : 'No especificado',
        proyecto: iProyecto >= 0 && cols[iProyecto] ? cols[iProyecto] : 'Proyecto Comunitario',
        estado: 'Pendiente', puntaje: 0, observacionesGenerales: '',
        fortalezas: '', areasOportunidad: '',
        scores: { crit1: null, crit2: null, crit3: null, crit4: null, crit5: null, crit6: null },
        notasProyecto: { problematica: '', realidad: '', acciones: '', decisiones: '', aprendio: '', propuesta: '', general: '' },
        preguntasIA: ''
      };

      dbParticipants.push(newStudent);
      adminUser.assignedIds.push(newStudent.id);

      const assignToEval = (evalNameRaw) => {
        if (!evalNameRaw || evalNameRaw.trim() === '') return;
        const normalized = evalNameRaw.trim().toUpperCase();
        if (evaluatorsMap.has(normalized)) {
          evaluatorsMap.get(normalized).assignedIds.push(newStudent.id);
        }
      };

      if (iEval1 >= 0) assignToEval(cols[iEval1]);
      if (iEval2 >= 0) assignToEval(cols[iEval2]);
      if (iEval3 >= 0) assignToEval(cols[iEval3]);
    }
  }

  return { dbEvaluadores, dbParticipants };
};

